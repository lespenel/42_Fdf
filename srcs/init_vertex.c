#include <fdf.h>

void	init_vertex(t_vertex *v)
{
	v->x = 0;
	v->y = 0;
	v->z = 0;
	v->w = 1;
	v->p_x = 0;
	v->p_y = 0;
	v->p_z = 0;
	v->p_w = 0;
	v->color = 0;
}
